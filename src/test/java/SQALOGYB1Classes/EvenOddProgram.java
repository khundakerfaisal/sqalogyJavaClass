package SQALOGYB1Classes;

public class EvenOddProgram {
    public static void main(String[] args) {
        for(int i=0;i<5;i++){
            if(i%2==0){
                System.out.println("This is even number" +i);
            }
            else {
                System.out.println("This is odd number " +i);
            }
        }
    }
}
