package Class4;

public class LoopClass {
    public static void main(String[] args) {
   //For Loop program
        for (int i=0;i<10;i++){
            System.out.println("Print value is : "+i);
        }
    //While loop program

        int i=0;
        while (i<=10){
            System.out.println("Print value is : "+i);
           i++;
        }
    //do-while loop program
        do {
            System.out.println("print value is "+i);
            i++;
        }while (i<10);
    }
}
